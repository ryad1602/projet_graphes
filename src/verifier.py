# 1. Gestion des chemins
_DIR = os.path.dirname(os.path.abspath(__file__))
BENCHMARK_PATH = os.path.join(_DIR, '..', 'benchmark', 'benchmark.xlsx')

def force_decode_g6(g6_str):
    """Fouille dans NetworkX pour trouver la fonction de lecture"""
    g6_str = g6_str.strip()
    
    # Liste des alias possibles selon les versions de NetworkX
    methods = [
        lambda: nx.from_graph6_string(g6_str),
        lambda: nx.readwrite.graph6.from_graph6_string(g6_str),
        lambda: nx.parse_graph6(g6_str)
    ]
    
    for method in methods:
        try:
            return method()
        except:
            continue
            
    raise Exception("Impossible de lire le Graph6. Vérifie l'installation : 'py -m pip install networkx'")

def verify_manual(target_id, g6_str=None):
    print(f"--- Chargement du benchmark Excel ---")
    try:
        conjectures = load_benchmark(BENCHMARK_PATH)
    except Exception as e:
        print(f"❌ Erreur Excel : {e}")
        return

    conj = next((c for c in conjectures if str(c.id) == str(target_id)), None)
    if not conj:
        print(f"❌ ID {target_id} introuvable.")
        return

    if not g6_str:
        print(f"❌ Aucun code G6 fourni.")
        return

    print(f"🧐 Analyse du graphe...")
    try:
        G = force_decode_g6(g6_str)
    except Exception as e:
        print(f"❌ Erreur : {e}")
        return

    # Calcul des valeurs réelles (X et Y)
    val_x = conj.calculate_invariant(G, conj.x_name)
    val_y = conj.calculate_invariant(G, conj.y_name)
    f_x = conj.evaluate_expression(val_x)
    
    # Calcul de la violation
    if conj.sign == "<=":
        violation = val_y - f_x
    else:
        violation = f_x - val_y

    print("\n" + "="*50)
    print(f"VÉRIFICATION MANUELLE - ID {target_id}")
    print(f"n = {G.number_of_nodes()} sommets")
    print("-" * 50)
    print(f"X ({conj.x_name}) = {val_x:.4f}")
    print(f"Y ({conj.y_name}) = {val_y:.4f}")
    print(f"Limite f(X) théorique = {f_x:.4f}")
    print("-" * 50)
    
    if violation > 1e-9:
        print(f"✅ VIOLATION DÉTECTÉE : {violation:.4f}")
    else:
        print(f"❌ AUCUNE VIOLATION.")
    print("="*50 + "\n")

